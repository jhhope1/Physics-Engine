cube::cube(vec resize) {
	cube C = cube();
	for (int i = 0; i < vertexnum; i += stride) {
		for (int j = 0; j < 3; j++) {
			vertices[i + j] = C.vertices[i + j] * resize.V[j];
		}
		for (int j = 3; j < stride; j++) {
			vertices[i + j] = C.vertices[i + j];
		}
	}
	for (int i = 0; i < indnum; i++) {
		indices[i] = C.indices[i];
	}
}
